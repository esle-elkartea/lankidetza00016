package com.code.aon.calculator;

public class DismissalExpenses {

    private double unfairDismissalCompensation;
    private double layoffAsUnfairDismissalCompensation;
    private double layoffCompensation;

    public double getUnfairDismissalCompensation() {
        return unfairDismissalCompensation;
    }

    public void setUnfairDismissalCompensation(double unfairDismissalCompensation) {
        this.unfairDismissalCompensation = unfairDismissalCompensation;
    }

    public double getLayoffAsUnfairDismissalCompensation() {
        return layoffAsUnfairDismissalCompensation;
    }

    public void setLayoffAsUnfairDismissalCompensation(double layoffAsUnfairDismissalCompensation) {
        this.layoffAsUnfairDismissalCompensation = layoffAsUnfairDismissalCompensation;
    }

    public double getLayoffCompensation() {
        return layoffCompensation;
    }

    public void setLayoffCompensation(double layoffCompensation) {
        this.layoffCompensation = layoffCompensation;
    }

}
